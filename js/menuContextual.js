function cargarMenuContextual() {
    var url = window.location.href;

    var arrayUAC = Object.keys(jsonNombreUAC);
    var encontrado = false;
    var UABEncontrada = "";
    for (var i = 0; i < arrayUAC.length; i++) {
        if (url.indexOf(arrayUAC[i]) > -1) {
            encontrado = true;
            UABEncontrada = arrayUAC[i];
            break;
        }
    }

    var botonVOlver = { "nombre": "Página principal", "value": "/" };


    if (encontrado) {
        jsonNombreUAC[UABEncontrada]["urls"].unshift(botonVOlver);
        document.getElementById("main_menu_container").style.display = "none";
        document.getElementById("main_menu_container_contextual").style.display = "block";

        var nuevoContenido = "";
        var contenidoViejo = document.getElementById("main_menu_container_contextual").innerHTML;
        for (var i = 0; i < Object.keys(jsonNombreUAC[UABEncontrada]["urls"]).length; i++) {
            nuevoContenido += insertarElementoMenu(jsonNombreUAC[UABEncontrada]["urls"][i]);
        }
        document.getElementById("main_menu_container_contextual").innerHTML = nuevoContenido + contenidoViejo;
    }

    function insertarElementoMenu(elemntoObjeto, depth = 0) {
        depth++;
        let nombreAgrupamiento = elemntoObjeto["nombreAgrupamiento"];

        if (nombreAgrupamiento) {
            let elementoHijo = "";
            let elementoHtml = "";
            for (var i = 0; i < Object.keys(elemntoObjeto["urls"]).length; i++) {
                elementoHijo += insertarElementoMenu(elemntoObjeto["urls"][i], depth);
                elementoHtml = `<div class="btn-group">
                        <div class="btn dropdown-toggle" data-toggle="dropdown">${nombreAgrupamiento}<span class="caret"></span></div>
                        <div class="dropdown-menu">
                          ${elementoHijo}                     
                        </div>
                      </div>`;
            }
            return elementoHtml;
        } else {
            let url = elemntoObjeto["value"];
            let nombre = elemntoObjeto["nombre"];
            if (depth > 1) {
                return `<a class="dropdown-item" href="https://ciencias.bogota.unal.edu.co${url}" target="_self" rel="noopener">${nombre}</a>`;
            } else {
                return `<div class="btn-group">
                            <a class="btn" href="https://ciencias.bogota.unal.edu.co${url}" target="_self" rel="noopener">${nombre}</a>
                          </div>`;
            }
        }
    }
}
